class Usuario {
  constructor(nome, email, cpf, telefone, dataNasc, endereco, bairro, cep, cidade, estado) {
    this.nome = nome;
    this.email = email;
    this.cpf = cpf;
    this.telefone = telefone;
    this.dataNasc = dataNasc;
    this.endereco = endereco;
    this.bairro = bairro;
    this.cep = cep;
    this.cidade = cidade;
    this.estado = estado;
  }
}

export default Usuario;
